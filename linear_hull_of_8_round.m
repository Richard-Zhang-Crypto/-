    for ou=1:255
        p=M01(in+1,:)*M12*M23*M34*M45*M56*M67*M78(:,ou+1);%计算8轮线性壳相关度
        a=bitand(bitshift(in, -8), 15);
        b=bitand(bitshift(in, -4), 15);
        c=bitand(in, 15);
        d= bitand(bitshift(ou, -4), 15);
        e= bitand(ou, 15);
        if abs(p)>2^-256
            score=256+log2(abs(p));%计算当前得分
            add=add+score;
            num=num+1;
            fprintf(fid, '@(8, 0x %x0%x0000%x, 0x %x%x%x%x0000, %.4e, %.4e) \n',a, b, c, e, d, d, bitxor(e,d), p, p);
        end
    end
end
