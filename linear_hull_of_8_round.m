        y3=bitand(i, 15);
        x1=bitand(bitshift(j, -8), 15);
        x2=bitand(bitshift(j, -4), 15);
        x3=bitand(j, 15);   
        if y2~=y3 && x2~=x3
            M23(i+1,j+1)=L(y1+1,x2+1)*L(y3+1,bitxor(x2,x3)+1)*L(bitxor(y3,y2)+1,x1+1)*L(y2+1,x1+1)*L(y2+1,x3+1);
