M01=zeros(4096,4096);
for i=0:4095
    for j=0:4095
        a=bitand(bitshift(i, -8), 15);
        b=bitand(bitshift(i, -4), 15);
        c=bitand(i, 15);
        x1=bitand(bitshift(j, -8), 15);
        x2=bitand(bitshift(j, -4), 15);
        x3=bitand(j, 15);   
        if a~=0 && b~=0 && c~=0
            M01(i+1,j+1)=L(a+1,x1+1)*L(b+1,x2+1)*L(c+1,x3+1);
        end
    end
end
%生成第0-1轮子LAT矩阵

M12=zeros(4096,4096);
for i=0:4095
    for j=0:4095
        x1=bitand(bitshift(i, -8), 15);
        x2=bitand(bitshift(i, -4), 15);
        x3=bitand(i, 15);
        y1=bitand(bitshift(j, -8), 15);
        y2=bitand(bitshift(j, -4), 15);
        y3=bitand(j, 15);   
        if x1~=x2 && x2~=x3
            M12(i+1,j+1)=L(x3+1,y1+1)*L(bitxor(x1,x2)+1,y2+1)*L(bitxor(x3,x2)+1,y3+1);
        end
    end
end
%生成第1-2轮子LAT矩阵

M23=zeros(4096,4096);
for i=0:4095
    for j=0:4095
        y1=bitand(bitshift(i, -8), 15);
