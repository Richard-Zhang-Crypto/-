        y1=bitand(bitshift(j, -8), 15);
        y2=bitand(bitshift(j, -4), 15);
        y3=bitand(j, 15);   
        if x2~=x3
            M34(i+1,j+1)=L(x1+1,y1+1)*L(x2+1,y3+1)*L(bitxor(x3,x2)+1,y2+1)*L(x1+1,y3+1)*L(x1+1,y2+1);
