        y2=bitand(bitshift(i, -4), 15);
        y3=bitand(i, 15);
        x1=bitand(bitshift(j, -4), 15);
        x2=bitand(j, 15);  
        if y2~=y1
            M45(i+1,j+1)=L(y2+1,x1+1)*L(bitxor(y1,y2)+1,x2+1)*L(y3+1,x2+1);
